
package E3;

public class Persona {
    
    protected String nombre;
    protected String cedula;
    protected String edad;
    protected String sexo;

    public Persona(String nombre, String cedula, String edad, String sexo) {
        this.nombre = nombre;
        this.cedula = cedula;
        this.edad = edad;
        this.sexo = sexo;
    }
    
    
}
